/// Converts an utf-8 char into a teletext encoded char
///
/// # Arguments
/// * `ch` - input char
/// * `subset` - Which character subset is currently used
pub fn char_to_teletext(
    ch: char,
    subset: NationalOptionCharacterSubset,
) -> Option<(TeletextChar, CharacterSet)> {
    subset
        .has_char(ch)
        .map(|code| (TeletextChar(code), CharacterSet::G0))
        .or_else(|| LATIN_TELETEXT_CHARS(ch).map(|res| (TeletextChar(res.0), res.1)))
}
