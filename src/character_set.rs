use crate::teletext::TeletextChar;

/// Enum of all possible language specific teletext character-sets
#[allow(dead_code)]
#[derive(Debug, Default, Copy, Clone)]
pub enum NationalOptionCharacterSubset {
    #[default]
    English,
    German,
    Swedish,
    Finnish,
    Hungarian,
    Italian,
    French,
    Portuguese,
    Spanish,
    Czech,
    Slovak,
}

/// Enum of all possible diacriticals
#[derive(Debug, Default, Copy, Clone)]
pub enum Diacritical {
    #[default]
    /// Character has no diacritical
    None,
    /// Diacritical: `
    Grave,
    /// Diacritical: ´
    Acute,
    /// Diacritical: ˆ
    Circumflex,
    /// Diacritical: ˜
    Tilde,
    /// Diacritical: ¯
    Macron,
    /// Diacritical: ˘
    Breve,
    /// Diacritical: ˙
    Dot,
    /// Diacritical:  ̈
    Umlaut,
    /// Diacritical:  ̣
    DotBelow,
    /// Diacritical:  ̊
    Ring,
    /// Diacritical:  ̧
    Cedilla,
    /// Diacritical: ̱
    #[allow(dead_code)]
    LowLine,
    /// Diacritical: ˝
    DoubleAcute,
    /// Diacritical: ˛
    Ogonek,
    /// Diacritical:  ̌
    Caron,
}

/// Marks in which type of character-set a character can be found
#[derive(Debug, Clone, Copy)]
pub enum CharacterSet {
    G0,
    G0WithDiacritical(Diacritical),
    G2,
    G3,
}

impl NationalOptionCharacterSubset {
    /// Checks if a [NationalOptionCharacterSubset] includes a specific character and if it includes it, the teletext encoding of that character is returned
    ///
    /// # Arguments
    /// * `ch` - Character that is checked
    fn has_char(&self, ch: char) -> Option<u8> {
        use NationalOptionCharacterSubset::*;

        match self {
            English => ENGLISH_TELETEXT_CHARS(ch),
            German => GERMAN_TELETEXT_CHARS(ch),
            _ => todo!("National character subset not implemented!"),
        }
    }
}

impl Diacritical {
    /// Encodes a diacritical into its teletext encoding
    pub fn code(&self) -> u8 {
        match self {
            Diacritical::None => 0x0,
            Diacritical::Grave => 0x1,
            Diacritical::Acute => 0x2,
            Diacritical::Circumflex => 0x3,
            Diacritical::Tilde => 0x4,
            Diacritical::Macron => 0x5,
            Diacritical::Breve => 0x6,
            Diacritical::Dot => 0x7,
            Diacritical::Umlaut => 0x8,
            Diacritical::DotBelow => 0x9,
            Diacritical::Ring => 0xA,
            Diacritical::Cedilla => 0xB,
            Diacritical::LowLine => 0xC,
            Diacritical::DoubleAcute => 0xD,
            Diacritical::Ogonek => 0xE,
            Diacritical::Caron => 0xF,
        }
    }
}

/// Returns the teletext encoding of a character if the english character-set contains it
static ENGLISH_TELETEXT_CHARS: fn(char) -> Option<u8> = |input_char| {
    Some(match input_char {
        '£' => 0x23,
        '$' => 0x24,
        '@' => 0x40,
        '←' => 0x5B,
        '½' => 0x5C,
        '→' => 0x5D,
        '↑' => 0x5E,
        '#' => 0x5F,
        '—' => 0x60,
        '¼' => 0x7B,
        '‖' => 0x7C,
        '¾' => 0x7D,
        '÷' => 0x7E,
        _ => return None,
    })
};

/// Returns the teletext encoding of a character if the german character-set contains it
